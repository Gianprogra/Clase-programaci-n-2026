# Ejercicio 7 Candidato 
# A partido amarillo
# B partido Morado
# N partido Naranja

Candidato = input('Elija el candidato: ') 

if Candidato == "A":  
    print('Usted escogio el partido "Amarillo" ')
elif Candidato == "M":
    print('Usted escogio el partido "Morado" ')
elif Candidato == "N":
    print('Usted escogió el partido "Naranja"')
else:
    print('Opción equivocada')